package AJavaFundamentals;

public class hFormat {
    public static void main(String[] args) {
        System.out.format("My height is %f, age is %d  and my name is %s", 6.1, 41,"Kamal");
        System.out.format("\nMy height is %.1f, age is %d  and my name is %s", 6.1, 41,"Kamal");
    // format is used to pass values and %.1 - rounding to 1 decimal etc.

    }
}
